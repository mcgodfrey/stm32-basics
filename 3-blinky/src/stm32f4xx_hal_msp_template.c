/* Includes ------------------------------------------------------------------*/
#include "stm32f4xx_hal.h"
#include "main.h"



void HAL_TIM_Base_MspInit(TIM_HandleTypeDef* htim_base)
{
  if(htim_base->Instance==TIM3)
  {
    /* Enable the TIM3 clock*/
    __HAL_RCC_TIM3_CLK_ENABLE();

    /**
     * TIM3 GPIO Configuration
    PA6     ------> TIM3_CH1
    PA7     ------> TIM3_CH2
    PB0     ------> TIM3_CH3
    */
    GPIO_InitTypeDef GPIO_InitStruct;

    GPIO_InitStruct.Mode = GPIO_MODE_AF_PP;
    GPIO_InitStruct.Pull = GPIO_NOPULL;
    GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_LOW;
    GPIO_InitStruct.Alternate = GPIO_AF2_TIM3;

    GPIO_InitStruct.Pin = INHA_Pin;
    HAL_GPIO_Init(INHA_Port, &GPIO_InitStruct);
    GPIO_InitStruct.Pin = INHB_Pin;
    HAL_GPIO_Init(INHB_Port, &GPIO_InitStruct);
    GPIO_InitStruct.Pin = INHC_Pin;
    HAL_GPIO_Init(INHC_Port, &GPIO_InitStruct);
  }
}

